<h1>FIM comparison tool</h1> 
<h2> Overview </h2>
The goal of the tool is to enable more rapid and seamless qualitative inundation map comparison workflows so that all of the qualitative insight gathering already being done using remotely sensed flood maps at NWC can happen in a frictionless way that is recorded and archived.

<h2>Project github page</h2>  
The codebase that inspired the idea for the comparison tool can be found <a href="https://github.com/dylanlee/Flood-Map-Comparison">here</a>. The initial code for prototype of the tool will be based on this codebase and will be available in sometime in March or April 2024. 

